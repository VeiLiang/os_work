// 根据ICON ID获取对应的ROM资源
APPROMRES*	AP_AppGetIconResource (UINT IconType, UINT IconID)
{
	// 缺省使用24点阵资源
	if(IconType >= AP_ICON_SIZE_COUNT)
		IconType =  AP_ICON_SIZE_24;

	if(IconID >= AP_ID_ICON_COUNT)
	{
		XM_ASSERT (0);
		return NULL;
	}

	return (APPROMRES*)&AppIconLibrary[IconType][IconID];
}





